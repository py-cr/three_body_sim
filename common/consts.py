"""
一小时多少秒
"""
SECONDS_PER_HOUR = 60 * 60
"""
一天多少秒
"""
SECONDS_PER_DAY = SECONDS_PER_HOUR * 24
"""
一周多少秒
"""
SECONDS_PER_WEEK = SECONDS_PER_DAY * 7
"""
一月多少秒（按照30天）
"""
SECONDS_PER_MONTH = SECONDS_PER_DAY * 30